public class EstruturaRepreticao {
    public static void main(String[] args){
        int cc = 1;
        while (cc<20){
            System.out.println(cc);
            cc++;
        }
        for (int i=0;i <= 20; i++){
            System.out.println(i);
        }
        int numero = 1;
        do{
            System.out.println(numero);
            numero++;
        }while(numero <= 20);
    }
}
