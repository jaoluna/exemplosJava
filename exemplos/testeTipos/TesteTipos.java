public class TesteTipos{
    public static void main(String[] args){
        int idade = 30;
        System.out.println("Valor númerico " + idade);
        String valor = Integer.toString(idade);
        System.out.println("Valor em String " + valor);

        String value = "30";
        System.out.println("O valor em String" + value);
        int age = Integer.parseInt(value);
        System.out.println("O valor numerico é " + age);

    }
}
